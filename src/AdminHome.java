    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        // TODO add your handling code here:
        String course = (String) courseNameComboBox.getSelectedItem();
        String department = (String) depNameComboBox.getSelectedItem();
        String rollNumber = rollNumberTextField.getText();
        String name = nameTextField.getText();
        String gender = (String) genderComboBox.getSelectedItem();
        String fatherName = fathersNameTextField.getText();
        
        try {
            //db credentials
            String user = "root";
            String password = "2222";
            String url = "jdbc:mysql://localhost:3306/studentresult";
            Class.forName("com.mysql.jdbc.Driver");
            
            Connection connectToDB = (Connection) DriverManager.getConnection(url, user, password);
            
            Statement sqlCommand = (Statement) connectToDB.createStatement();
            sqlCommand.executeUpdate("INSERT INTO students(course, department, roll_number, name, gender, father_name) VALUES('"+course+"', '"+department+"', '"+rollNumber+"', '"+name+"', '"+gender+"', '"+fatherName+"')");
            
            JOptionPane.showMessageDialog(null, "Data Insert Successful! Database Updated!");
            setVisible(false);
            new AdminHome().setVisible(true);
            
        } catch(ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "ClassNotFoundException Block Entered!");
            Logger.getLogger(AdminHome.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "SQL Error!");
            Logger.getLogger(AdminHome.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_createButtonActionPerformed

