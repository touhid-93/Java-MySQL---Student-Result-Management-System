    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        
        String rollNumber = rollNumberTextfield.getText();
        
        try {
             //db credentials
            String user = "root";
            String password = "2222";
            String url = "jdbc:mysql://localhost:3306/studentresult";
            Class.forName("com.mysql.jdbc.Driver");
            
            Connection connectToDB = (Connection) DriverManager.getConnection(url, user, password);
            
            Statement sqlCommand = (Statement) connectToDB.createStatement();
            ResultSet resultset = sqlCommand.executeQuery("SELECT * FROM students INNER JOIN result WHERE students.roll_number = '"+rollNumber+"' AND result.roll_number = '"+rollNumber+"'");
            
            while(resultset.next()) {
                nameTextfield.setText(resultset.getNString(2));
                fathersNameTextfield.setText(resultset.getNString(3));
                genderTextfield.setText(resultset.getString(4));
                depTextfield.setText(resultset.getNString(5));
                courseTextField.setText(resultset.getString(6));
                fathersNameTextfield.setText(resultset.getNString(7));
                
                datastructureTextfield.setText(resultset.getString(8));
                algorithmTextfield.setText(resultset.getNString(9));
                oopTextfield.setText(resultset.getString(10));
                databaseTextfield.setText(resultset.getNString(11));
                networkingTextfield.setText(resultset.getString(12));
            }
            
            int sub1 = Integer.parseInt(datastructureTextfield.getText());
            int sub2 = Integer.parseInt(algorithmTextfield.getText());
            int sub3 = Integer.parseInt(oopTextfield.getText());
            int sub4 = Integer.parseInt(databaseTextfield.getText());
            int sub5 = Integer.parseInt(networkingTextfield.getText());
            
            if(sub1 < 30 || sub2 < 30 || sub3 < 30 || sub4 < 30 || sub5 < 30) {
                remarksTextfield.setText("Failed");
            } else {
                remarksTextfield.setText("Passed");
            }
            
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "ClassNotFoundException Block Entered!");
            Logger.getLogger(StudentHome.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "SQL Error!");
            Logger.getLogger(StudentHome.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formComponentShown

